package com.bitutech.itemcategory;

public class itemCategoryGRN {
	
	private String batchNO;
	private String expiryDate;
	private String mrp;
	private String manufactureDetails;
	private String catagoryId;
	
	
	
	public final String getCatagoryId() {
		return catagoryId;
	}
	public final void setCatagoryId(String catagoryId) {
		this.catagoryId = catagoryId;
	}
	public final String getBatchNO() {
		return batchNO;
	}
	public final void setBatchNO(String batchNO) {
		this.batchNO = batchNO;
	}
	public final String getExpiryDate() {
		return expiryDate;
	}
	public final void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public final String getMrp() {
		return mrp;
	}
	public final void setMrp(String mrp) {
		this.mrp = mrp;
	}
	public final String getManufactureDetails() {
		return manufactureDetails;
	}
	public final void setManufactureDetails(String manufactureDetails) {
		this.manufactureDetails = manufactureDetails;
	}

}
